package Metodos;

public class MetodosString {

    /*Los metodos mas usados para los tipo String
    *
    * int length(): sabremos el numero de caracteres de un string
    *
    * boolean equals(String b): compara si son iguales por valor
    *
    * boolean equalsIgnoreCase(String b): compara si ambas son iguales ignorando mayusculas y minusculas
    *
    * int compareTo(String b): Compara contra la cadena del argumento devolviendo:
    *          + un valor negativo si la cadena es anterior a b
    *          + 0 si la cadena es igual a b
    *          + un valor positivo si la cadena es posterior a b
    *
    * String trim(): Crea un nuevo objeto eliminando el espacio en blanco que pudira haber al principio
    * o al final
    *
    * char charAt(int posicion): Estrae un caracter en la posicio indicada
    *
    * char[]toCharArray(): convierte la cadena en un arreglo de caracteres
    *
    * String substring(int a, int b): Extrae la sub-cadena entre las posiciones a y b
    *
    * String substring(int desde): Extrae la sub-cadena desde la posicion indicada
    *
    * int indexOf(String cadena): Indica en que posicion se encuentra el caracter o cadena
    * indicado por primera vez, buscando desde el principio
    *
    * int lastIndexOf(String cadena): Indica en que posicion se encuentra el caracter o cadena
    * indicado por primera vez, buscando desde el final
    *
    * boolean startWith(String prefijo): Dice si la cedena comienza en el prefijo indicado
    *
    * boolean endsWith(String prefijo): Dice si la cedena termina en el prefijo indicado
    *
    * String[] split(String patron): Divide la cedena en varias subcadenas utilizando el patron indicado
    * como separador*/
}

package Operadores;

public class TiposDeOperadores {
    public static void main(String[] args) {

        //OPERADORES ARITMETICOS
        /*Los operadores aritmeticos son todos los que usamos para hacer operaciones matematicas estos son:
        *  Multiplicar *
        *  Division /
        *  Resto % (Cuanto sobra de una division)
        *  Suma +
        *  Resta -
        *  Incremento ++
        *  Decremento --
        *  Menos Unario - (Para cambiar el signo a una exprecion)
        * */

        //OPERADORES COMBINADOS
        /*Estos operadores se utilizan para simplificar las operacion como los siguiente:
        *  -=
        *  +=
        *  /=
        *  *=
        * etc
        * */

        //INCREMENTO Y DECREMENTO
        /*Estos nos ayudan para incrementar de uno en uno el valor de nuestras variables, tenemos los pre y los
        * post incremento como los siguientes
        *              PRE
        *  ++a = Incrementa a en uno y devuleve a
        *     int i = 1;
        *     int j = ++1 // i = i + 1
        *              POST
        *  a++ = Devuelve a y luego incrementa uno
        *      int i = 1;
        *      int j = i++
        *      i = 3
        *      j = 2
        *
        * Y esto de igual forma para decrementar */

        //RELACIONALES
        /*
        *  <
        *  <=
        *  >
        *  >=
        *  ==
        *  !=
        * */

        //LOGICOS
        /* se utilizan los simbolos ya que java no soporta las palabras como operadores
        *
        *  && = and (Evalua ambas instanias)
        *  || = or  (Solo evalua una si es verdadera, si no evalua la siguiente)
        *  ! = not
        *
        */

        //OPERADORES UNARIOS
        /*
        * Estos operadores los utilizamos para cambiar el signo de un valor, asi como -5, -i, -90
        * y esto sigue la ley de signos + + + = +, - + - + = +, + + - = -, - + + = -
        * */
    }
}
